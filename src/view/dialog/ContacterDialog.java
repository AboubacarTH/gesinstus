    private void btn_envoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_envoyerActionPerformed
        String numero;
        numero = txt_contact.getText().replaceAll("\\.", "").trim().replaceAll("\\+227", "").replaceAll("00227", "");
        if(numero.length() == 0){
            JOptionPane.showMessageDialog(this, "Champ vide.", "Erreur de syntaxe !", JOptionPane.WARNING_MESSAGE);
        }else{
            configurationController.sendSMS("+" + "227" + numero, txt_message.getText());
            this.dispose();
        }
    }//GEN-LAST:event_btn_envoyerActionPerformed

